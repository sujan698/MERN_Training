//array store collection of value
//object store collection of key value pair
//object is a collection of key value pair
//key is a string
//value can be anything
//key value pair is separated by comma
//key value pair is separated by colon
//object is enclosed in curly braces

//object literal
let person = {  //object
    name: 'John', //key value pair
    age: 32, //key value pair
    city: 'New York' //key value pair
};  
console.log(person.name); //output: { name: 'John' }
console.log(person.age); //output: { age: 32 }
console.log(person.city); //output: { city: 'New York' }
console.log(person); //output: { name: 'John', age: 32, city: 'New York' }